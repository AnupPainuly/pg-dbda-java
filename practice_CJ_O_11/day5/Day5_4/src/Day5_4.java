import static java.lang.Math.*; //importing all the static fields of Math class from java.lang package.

public class Day5_4 {
	public static void main(String[] args) {
		System.out.println("value of PI "+PI);
		System.out.println("Max value "+max(44,477));
		System.out.println("Min value "+min(654,446));
		System.out.println("sq root "+sqrt(25.0));

	}

}
